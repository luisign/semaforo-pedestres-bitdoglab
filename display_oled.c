//INCLUSAO DE BIBLIOTECAS:
#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include <ctype.h>
#include "pico/stdlib.h"
#include "pico/binary_info.h"
#include "inc/ssd1306.h"
#include "hardware/i2c.h"

//DEFINICAO DE PINOS (LED RGB, BOTAO A E DISPLAY OLED EM I2C):
#define LED_R_PIN 13
#define LED_G_PIN 11
#define LED_B_PIN 12

#define BTN_A_PIN 5

const uint I2C_SDA = 14;
const uint I2C_SCL = 15;

//ARMAZENA O ESTADO DO BOTAO:
int A_state = 0;

//CONFIGURA O SINAL VERDE (ABERTO):
void SinalAberto(){
    gpio_put(LED_R_PIN, 0);
    gpio_put(LED_G_PIN, 1);
    gpio_put(LED_B_PIN, 0);   
}

//CONFIGURA O SINAL VERMELHO (FECHADO):
void SinalFechado(){
    gpio_put(LED_R_PIN, 1);
    gpio_put(LED_G_PIN, 0);
    gpio_put(LED_B_PIN, 0);
}

//EXIBE A MENSAGEM DO SINAL FECHADO NO DISPLAY:
void mensagemFechado(){
    struct render_area frame_area = {
        start_column : 0,
        end_column : ssd1306_width - 1,
        start_page : 0,
        end_page : ssd1306_n_pages - 1
    };

    calculate_render_area_buffer_length(&frame_area);

    uint8_t ssd[ssd1306_buffer_length];
    memset(ssd, 0, ssd1306_buffer_length);
    render_on_display(ssd, &frame_area);

    char *text[] = {
        "SINAL FECHADO",
        "AGUARDE"};

    int y = 0;
    for (uint i = 0; i < count_of(text); i++)
    {
        ssd1306_draw_string(ssd, 5, y, text[i]);
        y += 8;
    }
    render_on_display(ssd, &frame_area);
}

